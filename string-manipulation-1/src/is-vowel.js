/* exported isVowel */
/* if char is equal to any vowel, return true, otherwise return false */
function isVowel(char) {
  if (char === 'a' || char === 'e' || char === 'i' || char === 'u' || char === 'o' || char === 'A' || char === 'E' || char === 'I' || char === 'U' || char === 'O') {
    return true;
  } else {
    return false;
  }
}
